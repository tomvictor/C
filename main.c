int32_t get_int_32(const uint8_t *array, size_t start_index) {
    int32_t value = 0;
    value |= array[start_index];
    value |= array[start_index + 1] << 8;
    value |= array[start_index + 2] << 16;
    value |= array[start_index + 3] << 24;
    return value;
}


